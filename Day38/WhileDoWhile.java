package Pranjal.day38;

public class WhileDoWhile {

        public static void main(String[] args) {
            int x = 5;
            System.out.println("While");

            while (x < 5) {
                System.out.println("ok");
            }

            System.out.println("Do-while loop:");
            do {
                System.out.println("Hello");
            } while (x < 5);
        }
    }


